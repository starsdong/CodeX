#!/usr/bin/env python3
"""Build blended low-energy SCE predictions by interpolating endpoint yields.

The 3.0 GeV endpoint uses the original SCE fit with Rc and R extracted from data.
The 7.7 GeV endpoint uses T and muB from the 7.7 GeV GC fit together with the
exact 7.7 GeV GC radius and Rc/R fixed to the 3.0 GeV SCE ratio. Intermediate
energies are generated by log-linear interpolation of the predicted yields.
"""

from __future__ import annotations

import argparse
import csv
import math
from pathlib import Path

TARGET_ENERGIES = [3.0, 3.2, 3.5, 3.9, 4.5, 5.2, 7.7]
EPS = 1.0e-12


def energy_tag(energy_gev: float) -> str:
    return f"{energy_gev:.1f}".replace(".", "p")


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description="Build low-energy blended SCE predictions from 3.0 and 7.7 GeV endpoint yields."
    )
    parser.add_argument("--outdir", default="data/thermus_sce_blended")
    parser.add_argument(
        "--low-summary",
        default="data/thermus_sce_low_energy/prediction_summary.csv",
        help="Prediction summary containing the 3.0 GeV SCE endpoint row.",
    )
    parser.add_argument(
        "--low-points",
        default="data/thermus_sce_low_energy/prediction_points/sqrts_3p0GeV_points.csv",
        help="3.0 GeV SCE endpoint point file.",
    )
    parser.add_argument(
        "--high-summary",
        default="data/thermus_sce_blended/endpoint_7p7/prediction_summary.csv",
        help="Prediction summary containing the exact 7.7 GeV SCE endpoint row.",
    )
    parser.add_argument(
        "--high-points",
        default="data/thermus_sce_blended/endpoint_7p7/prediction_points/sqrts_7p7GeV_points.csv",
        help="7.7 GeV SCE endpoint point file.",
    )
    parser.add_argument(
        "--weight-power",
        type=float,
        default=2.0,
        help=(
            "Power-law exponent controlling how quickly the 7.7 GeV endpoint turns on. "
            "Values greater than 1 make the 7.7 GeV weight rise faster than a linear ramp."
        ),
    )
    parser.add_argument(
        "--match-ref-dir",
        default="",
        help=(
            "Optional directory of reference point CSV files used to calibrate a common "
            "weight at each intermediate energy. If set together with --match-particles, "
            "the explicit calibrated weights replace the power-law ramp."
        ),
    )
    parser.add_argument(
        "--match-particles",
        nargs="*",
        default=[],
        help="Particle names used to calibrate the common intermediate-energy weights.",
    )
    return parser.parse_args()


def read_summary_row(path: Path, energy_gev: float) -> dict[str, str]:
    with path.open(newline="", encoding="utf-8") as f:
        for row in csv.DictReader(f):
            if abs(float(row["energy_GeV"]) - energy_gev) < 1e-9:
                return row
    raise ValueError(f"No row for sqrt(sNN)={energy_gev} GeV found in {path}")


def read_points(path: Path) -> list[dict[str, str]]:
    with path.open(newline="", encoding="utf-8") as f:
        return list(csv.DictReader(f))


def interpolate_model_yield(y_low: float, y_high: float, frac: float) -> float:
    if frac <= 0.0:
        return y_low
    if frac >= 1.0:
        return y_high
    return math.exp((1.0 - frac) * math.log(max(y_low, EPS)) + frac * math.log(max(y_high, EPS)))


def effective_high_weight(raw_frac: float, weight_power: float) -> float:
    raw_frac = min(max(raw_frac, 0.0), 1.0)
    if weight_power <= 0.0:
        raise ValueError("weight_power must be positive")
    return 1.0 - (1.0 - raw_frac) ** weight_power


def load_reference_points(points_dir: Path) -> dict[tuple[float, str], float]:
    out: dict[tuple[float, str], float] = {}
    for path in sorted(points_dir.glob("sqrts_*GeV_points.csv")):
        with path.open(newline="", encoding="utf-8") as f:
            for row in csv.DictReader(f):
                if not row.get("model_yield"):
                    continue
                out[(float(row["energy_GeV"]), row["particle_name"])] = float(row["model_yield"])
    return out


def calibrated_weights(
    low_map: dict[str, dict[str, str]],
    high_map: dict[str, dict[str, str]],
    reference_dir: Path,
    particles: list[str],
) -> dict[float, float]:
    reference = load_reference_points(reference_dir)
    weights: dict[float, float] = {3.0: 0.0, 7.7: 1.0}
    for energy in TARGET_ENERGIES:
        if energy in (3.0, 7.7):
            continue
        numerator = 0.0
        denominator = 0.0
        used = []
        for particle in particles:
            y_ref = reference.get((energy, particle))
            low_row = low_map.get(particle)
            high_row = high_map.get(particle)
            if y_ref is None or low_row is None or high_row is None:
                continue
            y_low = float(low_row["model_yield"])
            y_high = float(high_row["model_yield"])
            a = math.log(max(y_high, EPS)) - math.log(max(y_low, EPS))
            b = math.log(max(y_low, EPS)) - math.log(max(y_ref, EPS))
            numerator += -a * b
            denominator += a * a
            used.append(particle)
        if denominator <= 0.0:
            raise ValueError(
                f"Cannot calibrate weight at {energy} GeV from particles {particles} in {reference_dir}"
            )
        weights[energy] = min(max(numerator / denominator, 0.0), 1.0)
    return weights


def write_endpoint_parameters(out_csv: Path, low_row: dict[str, str], high_row: dict[str, str]) -> None:
    with out_csv.open("w", newline="", encoding="utf-8") as f:
        writer = csv.DictWriter(
            f,
            fieldnames=[
                "energy_GeV",
                "T_MeV",
                "muB_MeV",
                "muQ_MeV",
                "gammaS",
                "Rc_fm",
                "R_fm",
                "dVdy_fm3",
                "source",
                "note",
            ],
        )
        writer.writeheader()
        writer.writerow(
            {
                "energy_GeV": low_row["energy_GeV"],
                "T_MeV": low_row["T_MeV"],
                "muB_MeV": low_row["muB_MeV"],
                "muQ_MeV": low_row["muQ_MeV"],
                "gammaS": low_row["gammaS"],
                "Rc_fm": low_row["Rc_fm"],
                "R_fm": low_row["R_fm"],
                "dVdy_fm3": low_row["dVdy_fm3"],
                "source": "3 GeV SCE fit endpoint",
                "note": "Exact 3 GeV SCE fit point with Rc and R extracted from data",
            }
        )
        writer.writerow(
            {
                "energy_GeV": high_row["energy_GeV"],
                "T_MeV": high_row["T_MeV"],
                "muB_MeV": high_row["muB_MeV"],
                "muQ_MeV": high_row["muQ_MeV"],
                "gammaS": high_row["gammaS"],
                "Rc_fm": high_row["Rc_fm"],
                "R_fm": high_row["R_fm"],
                "dVdy_fm3": high_row["dVdy_fm3"],
                "source": "7.7 GeV SCE endpoint with GC radius",
                "note": (
                    "Exact 7.7 GeV endpoint with T and muB from the 7.7 GeV GC fit, "
                    "R from the exact 7.7 GeV GC fit, and Rc/R fixed to the 3 GeV SCE ratio"
                ),
            }
        )


def write_interpolation_grid(
    out_csv: Path,
    weights_by_energy: dict[float, float],
    weight_power: float,
    calibration_note: str | None,
) -> None:
    with out_csv.open("w", newline="", encoding="utf-8") as f:
        writer = csv.DictWriter(
            f,
            fieldnames=[
                "energy_GeV",
                "raw_weight_7p7GeV",
                "weight_3GeV",
                "weight_7p7GeV",
                "weight_power",
                "interpolation_method",
                "note",
            ],
        )
        writer.writeheader()
        for energy in TARGET_ENERGIES:
            raw_frac = (energy - 3.0) / (7.7 - 3.0)
            raw_frac = min(max(raw_frac, 0.0), 1.0)
            frac = weights_by_energy[energy]
            writer.writerow(
                {
                    "energy_GeV": f"{energy:g}",
                    "raw_weight_7p7GeV": f"{raw_frac:.6f}",
                    "weight_3GeV": f"{1.0 - frac:.6f}",
                    "weight_7p7GeV": f"{frac:.6f}",
                    "weight_power": f"{weight_power:.6f}" if calibration_note is None else "",
                    "interpolation_method": (
                        "calibrated common log-linear interpolation weights from reference particles"
                        if calibration_note is not None
                        else "accelerated-endpoint log-linear interpolation of model yields in sqrt(sNN)"
                    ),
                    "note": calibration_note
                    if calibration_note is not None
                    else (
                        "Exact endpoint values are used at 3.0 and 7.7 GeV; the 7.7 GeV "
                        "weight rises faster than linear according to "
                        "1 - (1 - f_raw)^power"
                    ),
                }
            )


def main() -> None:
    args = parse_args()
    out_dir = Path(args.outdir)
    points_outdir = out_dir / "prediction_points"
    out_dir.mkdir(parents=True, exist_ok=True)
    points_outdir.mkdir(parents=True, exist_ok=True)

    low_summary = read_summary_row(Path(args.low_summary), 3.0)
    high_summary = read_summary_row(Path(args.high_summary), 7.7)
    low_points = read_points(Path(args.low_points))
    high_points = read_points(Path(args.high_points))

    low_map = {row["particle_name"]: row for row in low_points}
    high_map = {row["particle_name"]: row for row in high_points}
    common_particles = [row["particle_name"] for row in low_points if row["particle_name"] in high_map]
    if len(common_particles) != len(low_points) or len(common_particles) != len(high_points):
        missing_low = sorted(set(high_map) - set(low_map))
        missing_high = sorted(set(low_map) - set(high_map))
        raise ValueError(
            "Endpoint particle lists do not match. "
            f"Missing in low endpoint: {missing_low}; missing in high endpoint: {missing_high}"
        )

    if args.match_ref_dir and args.match_particles:
        weights_by_energy = calibrated_weights(
            low_map,
            high_map,
            Path(args.match_ref_dir),
            args.match_particles,
        )
        calibration_note = (
            "Exact endpoint values are used at 3.0 and 7.7 GeV; intermediate common weights are "
            f"calibrated at each energy by minimizing the combined log-space mismatch to reference "
            f"particles {', '.join(args.match_particles)} in {args.match_ref_dir}"
        )
    else:
        weights_by_energy = {}
        for energy in TARGET_ENERGIES:
            raw_frac = (energy - 3.0) / (7.7 - 3.0)
            raw_frac = min(max(raw_frac, 0.0), 1.0)
            weights_by_energy[energy] = effective_high_weight(raw_frac, args.weight_power)
        calibration_note = None

    summary_rows: list[dict[str, str]] = []
    for energy in TARGET_ENERGIES:
        raw_frac = (energy - 3.0) / (7.7 - 3.0)
        raw_frac = min(max(raw_frac, 0.0), 1.0)
        frac = weights_by_energy[energy]

        point_rows = []
        for particle in common_particles:
            low_row = low_map[particle]
            high_row = high_map[particle]
            y_low = float(low_row["model_yield"])
            y_high = float(high_row["model_yield"])
            model_yield = interpolate_model_yield(y_low, y_high, frac)
            point_rows.append(
                {
                    "energy_GeV": f"{energy:.6f}",
                    "pdg_id": low_row["pdg_id"],
                    "particle_name": particle,
                    "model_yield": f"{model_yield:.6f}",
                }
            )

        points_file = points_outdir / f"sqrts_{energy_tag(energy)}GeV_points.csv"
        with points_file.open("w", newline="", encoding="utf-8") as f:
            writer = csv.DictWriter(f, fieldnames=["energy_GeV", "pdg_id", "particle_name", "model_yield"])
            writer.writeheader()
            writer.writerows(point_rows)

        summary_rows.append(
            {
                "energy_GeV": f"{energy:.6f}",
                "source_3GeV_file": args.low_points,
                "source_7p7GeV_file": args.high_points,
                "raw_weight_7p7GeV": f"{raw_frac:.6f}",
                "weight_3GeV": f"{1.0 - frac:.6f}",
                "weight_7p7GeV": f"{frac:.6f}",
                "weight_power": "" if calibration_note is not None else f"{args.weight_power:.6f}",
                "interpolation_method": (
                    "calibrated common log-linear interpolation weights from reference particles"
                    if calibration_note is not None
                    else "accelerated-endpoint log-linear interpolation of model yields in sqrt(sNN)"
                ),
                "points_file": str(points_file),
                "note": (
                    "3.0 GeV uses the exact SCE fit point; 7.7 GeV uses the exact GC-radius "
                    "SCE endpoint; intermediate points interpolate the predicted yields directly "
                    f"with the common calibrated weight from {', '.join(args.match_particles)}"
                    if calibration_note is not None
                    else
                    "3.0 GeV uses the exact SCE fit point; 7.7 GeV uses the exact GC-radius "
                    "SCE endpoint; intermediate points interpolate the predicted yields directly "
                    f"with the 7.7 GeV endpoint weight given by 1 - (1 - f_raw)^{args.weight_power:g}"
                ),
            }
        )

    with (out_dir / "prediction_summary.csv").open("w", newline="", encoding="utf-8") as f:
        writer = csv.DictWriter(
            f,
            fieldnames=[
                "energy_GeV",
                "source_3GeV_file",
                "source_7p7GeV_file",
                "raw_weight_7p7GeV",
                "weight_3GeV",
                "weight_7p7GeV",
                "weight_power",
                "interpolation_method",
                "points_file",
                "note",
            ],
        )
        writer.writeheader()
        writer.writerows(summary_rows)

    write_endpoint_parameters(out_dir / "endpoint_parameters.csv", low_summary, high_summary)
    write_interpolation_grid(out_dir / "interpolation_grid.csv", weights_by_energy, args.weight_power, calibration_note)

    print(f"wrote {out_dir / 'endpoint_parameters.csv'}")
    print(f"wrote {out_dir / 'interpolation_grid.csv'}")
    print(f"wrote {out_dir / 'prediction_summary.csv'}")
    print(f"wrote {len(summary_rows)} prediction point files to {points_outdir}")


if __name__ == "__main__":
    main()
